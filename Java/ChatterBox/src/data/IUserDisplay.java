package data;

/*******************************************************************************
 * Interface for a component that displays a list of users.
 ******************************************************************************/
public interface IUserDisplay
{
    /***************************************************************************
     * Adds a user to the display.
     * 
     * @param user
     **************************************************************************/
    public void addUser( IUser user );

    /***************************************************************************
     * Removes a user from the display.
     * 
     * @param user
     **************************************************************************/
    public void removeUser( IUser user );

    /***************************************************************************
     * Updates the display.
     **************************************************************************/
    public void updateDisplay();
}
