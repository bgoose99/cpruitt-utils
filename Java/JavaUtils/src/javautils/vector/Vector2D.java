package javautils.vector;

import java.awt.Point;

/*******************************************************************************
 * This class represents a 2-dimensional vector.
 ******************************************************************************/
public class Vector2D
{
    public double x;
    public double y;

    /***************************************************************************
     * Constructor
     **************************************************************************/
    public Vector2D()
    {
        this( 0.0, 0.0 );
    }

    /***************************************************************************
     * Constructor
     * 
     * @param p
     **************************************************************************/
    public Vector2D( Point p )
    {
        this( p.x, p.y );
    }

    /***************************************************************************
     * Constructor
     * 
     * @param x
     * @param y
     **************************************************************************/
    public Vector2D( double x, double y )
    {
        this.x = x;
        this.y = y;
    }

    /***************************************************************************
     * Returns the magnitude of this 2-D vector.
     * 
     * @return
     **************************************************************************/
    public double getMagnitude()
    {
        return Math.sqrt( x * x + y * y );
    }
}
