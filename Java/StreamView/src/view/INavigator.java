package view;

/*******************************************************************************
 * Simple navigation interface.
 ******************************************************************************/
public interface INavigator
{
    /***************************************************************************
     * Returns the navigator label's text.
     * 
     * @return
     **************************************************************************/
    String getLabelText();

    /***************************************************************************
     * Sets the navigator label's text.
     * 
     * @param text
     **************************************************************************/
    void setLabelText( String text );

    /***************************************************************************
     * Returns the value of the goto field.
     * 
     * @return
     **************************************************************************/
    String getGotoText();

    /***************************************************************************
     * Sets the value of the goto field.
     * 
     * @param text
     **************************************************************************/
    void setGotoText( String text );

    /***************************************************************************
     * Returns the progress.
     * 
     * @return
     **************************************************************************/
    int getProgress();

    /***************************************************************************
     * Sets the progress.
     * 
     * @param value
     **************************************************************************/
    void setProgress( int value );

    /***************************************************************************
     * Returns true if the previous button is enabled, false otherwise.
     * 
     * @return
     **************************************************************************/
    boolean getPrevEnabled();

    /***************************************************************************
     * Sets the state of the previous button.
     * 
     * @param value
     **************************************************************************/
    void setPrevEnabled( boolean value );

    /***************************************************************************
     * Returns true if the next button is enabled, false otherwise.
     * 
     * @return
     **************************************************************************/
    boolean getNextEnabled();

    /***************************************************************************
     * Sets the state of the next button.
     * 
     * @param value
     **************************************************************************/
    void setNextEnabled( boolean value );

    /***************************************************************************
     * Returns true if the goto button is enabled, false otherwise.
     * 
     * @return
     **************************************************************************/
    boolean getGotoEnabled();

    /***************************************************************************
     * Sets the state of the goto button.
     * 
     * @param value
     **************************************************************************/
    void setGotoEnabled( boolean value );
}
