package javautils.io;

/*******************************************************************************
 * Generic serializer interface.
 * 
 * @param <T>
 ******************************************************************************/
public interface ISerializer<T>
{
    /***************************************************************************
     * Reads an item.
     * 
     * @return
     **************************************************************************/
    public T readItem() throws Exception;

    /***************************************************************************
     * Writes an item.
     * 
     * @param item
     **************************************************************************/
    public void writeItem( T item ) throws Exception;
}
