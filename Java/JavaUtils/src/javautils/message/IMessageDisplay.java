package javautils.message;

/*******************************************************************************
 * Message display interface.
 ******************************************************************************/
public interface IMessageDisplay
{
    /***************************************************************************
     * Adds a message to this display.
     * 
     * @param msg
     **************************************************************************/
    public void addMessage( IMessage msg );

    /***************************************************************************
     * Clears the content of the display.
     **************************************************************************/
    public void clear();
}
